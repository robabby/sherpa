/**
 * Sherpa Component Catalog
 *
 * Machine-readable inventory of all components exported from @sherpa/studio-ui.
 * Each entry documents the component's visual pattern, domain, tokens, and
 * composition relationships.
 *
 * Sync test: src/__tests__/catalog-sync.test.ts
 */

export interface ComponentCatalogEntry {
  /** PascalCase export name */
  name: string
  /** One-line description */
  description: string
  /** Which design pattern it implements (null if composite/unique) */
  pattern:
    | "glass-panel"
    | "status-indicator"
    | "data-readout"
    | "timeline"
    | "pipeline-bar"
    | "card-list"
    | "section-header"
    | "badge"
    | null
  /** Key variant prop values */
  variants?: string[]
  /** CSS custom property names referenced (without --) */
  tokens: string[]
  /** Other studio-ui components commonly composed with */
  composedWith?: string[]
  /** Whether this is a client component ("use client") */
  isClient: boolean
  /** Functional domain */
  domain:
    | "governance"
    | "data-viz"
    | "hub"
    | "content"
    | "layout"
    | "agents"
    | "domain-panels"
  /** Source module path (matches barrel export, without ./ prefix) */
  source: string
}

