// ── Lookup helpers ─────────────────────────────────────────────────

/** Find a component by PascalCase name */
export function getComponent(
  name: string
): ComponentCatalogEntry | undefined {
  return COMPONENT_CATALOG.find((c) => c.name === name)
}

/** List all components implementing a given pattern */
export function getComponentsByPattern(
  pattern: ComponentCatalogEntry["pattern"]
): ComponentCatalogEntry[] {
  return COMPONENT_CATALOG.filter((c) => c.pattern === pattern)
}

/** List all components in a given domain */
export function getComponentsByDomain(
  domain: ComponentCatalogEntry["domain"]
): ComponentCatalogEntry[] {
  return COMPONENT_CATALOG.filter((c) => c.domain === domain)
}

/** List all components that compose with a given component */
export function getComponentsByComposition(
  name: string
): ComponentCatalogEntry[] {
  return COMPONENT_CATALOG.filter((c) => c.composedWith?.includes(name))
}
